# З клавіатури задано кількість елементів масиву N та значення
# кожного елемента (масив складається з цілих чисел). Знайти суму індексів
# елементів, значення яких є простими числами. (Просте число — це
# натуральне число, яке має рівно два

n = int(input("Введіть кількість елементів масиву N: "))
arr = []
for i in range(n):
    arr.append(int(input(f"Введіть значення {i+1} елемента масиву: ")))
sum = 0
for i in range(n):
    if arr[i] % 2 != 0 and arr[i] % 3 != 0 and arr[i] % 5 != 0:
        sum += i
print("Сума індексів елементів, значення яких є простими числами:", sum)