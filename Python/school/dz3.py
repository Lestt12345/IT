# Задача 1
# З клавіатури задано кількість елементів масиву N та значення
# кожного елемента (масив складається з цілих чисел). Вивести значення
# найменшого елементу масиву та кількість таких елементів.

n = int(input("Введіть кількість елементів масиву N: "))
arr = []
for i in range(n):
    arr.append(int(input(f"Введіть значення {i+1} елемента масиву: ")))
print("Найменший елемент масиву:", min(arr))
print("Кількість таких елементів:", arr.count(min(arr)))

print("________________________________________________\n")

