arr = []
for i in range(n):
    arr.append(int(input(f"Введіть значення {i+1} елемента масиву: ")))
for i in range(1, n - 1):
    if arr[i] < arr[i - 1] and arr[i] < arr[i + 1]:
        print(arr[i], end=" ")
        
